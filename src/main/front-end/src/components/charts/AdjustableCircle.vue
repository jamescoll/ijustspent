<template>
  <div>
    <svg width="500" height="300"></svg>
    <br>
    <input
      type="range"
      v-model="circleSize"
      min="1"
      max="100"
      step="1"
    >
  </div>
</template>

<script>
import * as d3 from 'd3'
export default {
  name: 'adjustable-circle',
  data: function () {
    return {
      circleSize: 50
    }
  },
  mounted: function (createElement) {
    var svg = d3.select(this.$el).select('svg')
    this.circle = svg
      .append('circle')
      .attr('cx', '250')
      .attr('cy', '150')
      .attr('r', this.circleSize)
  },
  watch: {
    circleSize: function (newValue) {
      this.circle
        .attr('r', newValue)
    }
  }
}
</script>

